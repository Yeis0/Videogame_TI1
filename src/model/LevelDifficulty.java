package model;

/**
 * This enum represents the difficulty of the level.
 * @author Yeison Antonio Rodriguez Zuluaga
 */
public enum LevelDifficulty {
    /**
     *The level is easy, which means that the score added by the treasures is bigger than the score added by the enemies.
     */
    EASY,
    /**
     * The level is medium, which means that the score added by the treasures is equal than the score added by the enemies.
     */
    MEDIUM,
    /**
     * The level is hard, which means that the score added by the treasures is smaller than the score added by the enemies.
     */
    HARD
    
}
